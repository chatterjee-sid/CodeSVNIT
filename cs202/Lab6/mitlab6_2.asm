;8b / 8b division
lda 00h
mov l,a
mvi h,0
lda 01h
mov b,a
mvi c,8
loop: call shifter
dcr c
jnz loop
shld 03h; quotient at 0003H,remainder at 0004H
hlt

shifter: dad h
mov a,h
sub b
jp fine
add b
ret
fine: mov h,a
inr l
ret

